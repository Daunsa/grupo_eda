public class Ejemplo_Variables{
	public static void main(String[] args){
		System.out.println("tenemos muchos tipos variables");
		int entero= 2, entero1= 21;
		final int enteroconstante= 6;
		float flotante= 19.86f;
		String string= "HOla";
		char caracter= 'a';
		boolean booleano= true;

		System.out.println("entero: "+entero);
		System.out.println("entero 1: "+entero1);
		System.out.println("enteroconstante: "+enteroconstante);
		System.out.println("flotante: "+flotante);
		System.out.println("string: "+string);
		System.out.println("caracter: "+caracter);
		System.out.println("booleano: "+booleano);
		System.out.println("sumando: "+(entero+entero1));


	}
	
}

