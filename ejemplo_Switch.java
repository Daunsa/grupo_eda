public class ejemplo_Switch {
	public static void main(String[]args) {
		int boleto = 2;
		switch (boleto) {
		  case 1:
		    System.out.println("Boleto perdedor");
		    break;
		  case 2:
		    System.out.println("Boleto ganador");
		    break;
		  case 3:
		    System.out.println("Boleto perdedor");
		    break;
		  case 4:
		    System.out.println("Boleto perdedor");
		    break;
		  case 5:
		    System.out.println("Boleto perdedor");
		    break;
		}
	}
}